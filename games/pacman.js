/* Next, create the play board for the game, 
which is a 28x28 grid, where each grid square
is a 15px by 15px square. */
function createGrid() {

    console.log("layout has " + layout.length + " squares");
    //for each square
    for (let i = 0; i < layout.length; i++) {
        // add to grid
        const square = document.createElement('div');
        // add to squares array for future access
        square.classList.add('pacman-square');
        grid.appendChild(square);
        squares.push(square);
        console.log("Added " + i + "th square");
    }
}

